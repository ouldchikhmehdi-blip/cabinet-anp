export const pct = (a, b) => b === 0 ? '0' : (((a - b) / b) * 100).toFixed(1)
export const diffLabel = (a, b, ref) => {
  const d = pct(a, b)
  return (d > 0 ? '↑ +' : '↓ ') + d + '% vs ' + ref
}
export const diffColor = (a, b, invert = false) => {
  const d = parseFloat(pct(a, b))
  if (invert) return d > 0 ? 'down' : 'up'
  return d > 0 ? 'up' : 'down'
}